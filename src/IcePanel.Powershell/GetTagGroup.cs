using System.Management.Automation;
using IcePanel.Api.Models;

namespace IcePanel.Powershell;

[Cmdlet(VerbsCommon.Get, "IcePanelTagGroup")]
[OutputType(typeof(TagGroup))]
public class GetTagGroup : IcePanelCmdlet
{
    [Parameter(
        Mandatory = false,
        ValueFromPipeline = true,
        ValueFromPipelineByPropertyName = true)]
    public Landscape[]? Landscape { get; set; }

    [Parameter(
        Mandatory = false,
        Position = 0,
        ValueFromPipelineByPropertyName = true)]
    public string? LandscapeId { get; set; }

    [Parameter(
        Mandatory = false,
        Position = 0,
        ValueFromPipelineByPropertyName = true)]
    public string? TagGroupId { get; set; }

    [Parameter(
        Mandatory = false,
        Position = 1,
        ValueFromPipelineByPropertyName = true)]
    public string? Version { get; set; } = "latest";

    protected override void ProcessRecord()
    {
        var api = GetApiClient();

        var landscapeIds = LandscapeId != null ? new[] { LandscapeId } : Landscape?.Select(p => p.Id);
        if (landscapeIds == null) throw new ArgumentException("Landscape need to be set");

        foreach (var lid in landscapeIds)
            if (TagGroupId != null)
            {
                var result = RunSync(() => api.Landscapes[lid].Versions[Version].TagGroups[TagGroupId].GetAsWithTagGroupGetResponseAsync());
                WriteObject(result.TagGroup);
            }
            else
            {
                var result = RunSync(() => api.Landscapes[lid].Versions[Version].TagGroups.GetAsTagGroupsGetResponseAsync());
                WriteObject(result.TagGroups);
            }
    }
}